def train_diffusion_model(num_epochs_diffusion=64):

    print("Starting DDPM3D training...")
    diffusion_model = DDPM3D().to(device)
    diffusion_model = nn.DataParallel(diffusion_model, device_ids=device_ids)
    diffusion_optimizer = optim.Adam(diffusion_model.parameters(), lr=learning_rate)
    mse_loss = nn.MSELoss()
    best_loss_diffusion = float('inf')
    early_stopping_counter_diffusion = 0
    saved_diffusion_models = []
    os.makedirs(model_save_path, exist_ok=True)

    best_model_path = os.path.join(model_save_path, 'best_diffusion_model.pth')

    for epoch_idx in range(num_epochs_diffusion):
        print(f"\n=== Diffusion Epoch {epoch_idx + 1}/{num_epochs_diffusion} ===")
        diffusion_model.train()
        epoch_running_loss = 0.0
        batch_count = 0

        for batch_idx, (images, _) in enumerate(train_loader_recon):
            images = images.to(device)

            # Add the occlusion 
            corrupted_images = images.clone()
            for i in range(images.size(0)):
                img = corrupted_images[i]
                _, D, H, W = img.shape
                d, h, w = int(D * 0.5), int(H * 0.3), int(W * 0.5)
                
                # Calculate central starting indice
                x = (D - d) // 2
                y = (H - h) // 2
                z = (W - w) // 2
                
                # Apply central cube corruption
                img[:, x:x + d, y:y + h, z:z + w] = 0


            # Add noise to the corrupted images
            t = torch.randint(low=0, high=diffusion_model.module.timesteps, size=(corrupted_images.size(0),), device=device)
            x_t, _ = diffusion_model.module.add_noise(corrupted_images, t)
            reconstructed_images = diffusion_model(x_t, t)

            # Compute loss using ground truth images
            loss = mse_loss(reconstructed_images, images)
            diffusion_optimizer.zero_grad()
            loss.backward()
            diffusion_optimizer.step()

            epoch_running_loss += loss.item()
            batch_count += 1
            if (batch_idx + 1) % 10 == 0:
                avg_loss = epoch_running_loss / batch_count
                print(f"Batch {batch_idx + 1}/{len(train_loader_recon)} - Avg Loss: {avg_loss:.4f}")

        avg_loss_epoch = epoch_running_loss / batch_count
        print(f"Epoch [{epoch_idx + 1}/{num_epochs_diffusion}], Avg Loss: {avg_loss_epoch:.4f}")

        if (epoch_idx + 1) % model_save_frequency == 0:
            model_path = f'{model_save_path}/diffusion_epoch_{epoch_idx + 1}.pth'
            torch.save(diffusion_model.state_dict(), model_path)
            saved_diffusion_models.append(model_path)
            if len(saved_diffusion_models) > max_models_to_keep:
                os.remove(saved_diffusion_models.pop(0))

        if avg_loss_epoch < best_loss_diffusion:
            best_loss_diffusion = avg_loss_epoch
            torch.save(diffusion_model.module.state_dict(), best_model_path)
            early_stopping_counter_diffusion = 0
        else:
            early_stopping_counter_diffusion += 1

        if early_stopping_counter_diffusion >= patience:
            print("Early stopping triggered.")
            break

    print("Finished training DDPM3D.")

